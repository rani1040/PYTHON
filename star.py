# Write a program to draw a star using a turtle?
import turtle
turtle.Screen().bgcolor("cyan")
turtle.Screen().setup(500,500)
turtle.forward(100)
turtle.left(120)
turtle.forward(100)
turtle.left(120)
turtle.forward(100)
turtle.penup()
turtle.right(150)
turtle.forward(50)
turtle.pendown()
turtle.right(90)
turtle.forward(100)
turtle.right(120)
turtle.forward(100)
turtle.right(120)
turtle.forward(100)
turtle.right(120)
turtle.done()